    private void loadData() throws ClassNotFoundException {
        List<models.Orders> orderList = orderController.GetAllOrderHistoryData();
    
        // Clear existing rows from the table
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
    
        // Populate the table with the retrieved data
        for (models.Orders orders : orderList) {
            Object[] rowData = {orders.getOrderId(),orders.getProductName(), orders.getOrderQty(), orders.getTotal(), orders.getProductName(), orders.getOrderDate(), orders.getOrderSelesai()};
            model.addRow(rowData);
        }
    }




 

