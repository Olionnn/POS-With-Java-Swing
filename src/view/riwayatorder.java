    private void dataComboBox() {
        try {
            // Get product data from the controller
            List<models.Product> productList = productController.GetAllproductData();
    
            NamaProduk.removeAllItems();
    
            for (models.Product product : productList) {
                NamaProduk.addItem(product.getProductName());
                orderMap.put(product.getProductName(), product.getProductId());
            }
        } catch (ClassNotFoundException e) {
            e.printStackTrace();
        }
    }
    private void DeleteActionPerformed(ActionEvent evt, int userId) throws NumberFormatException, ClassNotFoundException, SQLException {
        dispose();
        this.setVisible(false);
        orderController.deleteOrderData(Integer.parseInt(orderId));
        riwayatorder rw = new riwayatorder(userId);
        rw.setVisible(true);
        rw.setTitle("SUI ORDER HISTORY");
        
    }

    private void TambahActionPerformed(ActionEvent evt, int userId) throws NumberFormatException, ClassNotFoundException, SQLException {
        String selectedProduct = NamaProduk.getSelectedItem().toString();
        int selectedProductId = orderMap.get(selectedProduct);
        String jum = Deskripsi.getText();
        int orderQty = Integer.parseInt(jum);
        
        dispose();
        this.setVisible(false);
        orderController.updateOrderHistoryData(Integer.parseInt(orderId), selectedProductId, orderQty, userId);
        riwayatorder rw = new riwayatorder(userId);
        rw.setVisible(true);
        rw.setTitle("SUII RIWAYAT ORDER");
    }
