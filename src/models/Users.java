package models;

public class Users {
    private int userId;
    private String userName, userEmail, userPassword, userPhone;



    public int getUserid() {
        return userId;
    }

    public String getUserName() {
        return userName;
    }
    public String getUserEmail() {
        return userEmail;
    }
    public String getUserPassword() {
        return userPassword;
    }
    public String getUserPhone() {
        return userPhone;
    }


}
